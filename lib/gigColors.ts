// Helper functions with additional utilities
export const getContrastColor = (hexColor: string): string => {
  // Remove the # if present
  const hex = hexColor.replace("#", "");

  // Convert to RGB
  const r = parseInt(hex.substr(0, 2), 16);
  const g = parseInt(hex.substr(2, 2), 16);
  const b = parseInt(hex.substr(4, 2), 16);

  // Calculate luminance (WCAG formula)
  const luminance = (0.299 * r + 0.587 * g + 0.114 * b) / 255;

  // Return black or white based on luminance
  return luminance > 0.5 ? "#000000" : "#FFFFFF";
};

export const getAccessibleTextColor = (backgroundColor: string, preferredColor?: string): string => {
  const contrastColor = getContrastColor(backgroundColor);
  
  if (!preferredColor) return contrastColor;
  
  // Check if preferred color has enough contrast
  const bgHex = backgroundColor.replace("#", "");
  const textHex = preferredColor.replace("#", "");
  
  const bgR = parseInt(bgHex.substr(0, 2), 16);
  const bgG = parseInt(bgHex.substr(2, 2), 16);
  const bgB = parseInt(bgHex.substr(4, 2), 16);
  
  const textR = parseInt(textHex.substr(0, 2), 16);
  const textG = parseInt(textHex.substr(2, 2), 16);
  const textB = parseInt(textHex.substr(4, 2), 16);
  
  // Calculate contrast ratio (WCAG)
  const bgL = (0.2126 * bgR + 0.7152 * bgG + 0.0722 * bgB) / 255;
  const textL = (0.2126 * textR + 0.7152 * textG + 0.0722 * textB) / 255;
  
  const lighter = Math.max(bgL, textL);
  const darker = Math.min(bgL, textL);
  const contrast = (lighter + 0.05) / (darker + 0.05);
  
  // Return preferred color if contrast >= 4.5 (WCAG AA standard)
  return contrast >= 4.5 ? preferredColor : contrastColor;
};

